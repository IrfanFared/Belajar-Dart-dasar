/// == sama dengan
/// != tidak sama dengan
/// < kurang dari
/// > lebih dari
/// <= kurang dari sama dengan
/// >= lebih dari saka dengan
/// bisa digunakan untuk tipe data string dan num/ int

void main() {
  var data1 = 1;
  var dara2 = 2;

  var samadengan = data1 == dara2;

  var tidaksamadengan = data1 != dara2;

  print(samadengan);
  print(tidaksamadengan);
}
