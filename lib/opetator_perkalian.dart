/// operrato aritmatika ada banyak sekali seperti pejumlahan
/// pengurangan, pembagian,dan lain lain
/// tanda + untuk penjumlah
/// - untuk pengurangan
/// / untuk pembagian
/// * untuk perkalian
/// contohnya dibawah

void main() {
  var data1 = 10;
  var data2 = 2;

  var penjumlahan = data2 + data1;
  var pengurangan = data1 - data2;
  var perkalian = data1 * data2;
  var pembagian = data1 / data2;
  var sisabagi = data1 % data2;

  print(penjumlahan);
  print(pengurangan);
  print(perkalian);
  print(pembagian);
  print(sisabagi);
}
